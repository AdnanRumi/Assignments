package stringClass;

public class String_Manipulation_Length {

	public static void main(String[] args) {
		
		String bio = "AY was born in Germany, studied in Turkey, and worked in various countries as an educator and peace activist.";
		
		System.out.println(bio.length());
		
		int a = bio.length();
		
		a = a * a;
		
		System.out.println(a);
		
		String b = bio;
		
		System.out.println(bio +"\n" + b);
		
		
		
		

	}

}
