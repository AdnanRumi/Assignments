package methods_week_2;

public class Methods_101 {
	
	public static void main (String[] args) {
		
		love();
		
		spreadPeace();
		
		empathy();
		
		
	}
	
	public static void spreadPeace () {
		
		System.out.println("Inside spreadPeace method");
	}
	
	public static void empathy() {
		
		System.out.println("A real human being is one who does not hurt the feelings of others.");
	}
	
	public static void love() {
		
		System.out.println("Love is a talisman that can open all doors.");
	}
}
