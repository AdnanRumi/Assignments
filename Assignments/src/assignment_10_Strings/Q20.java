package assignment_10_Strings;

public class Q20 {

	public static void main(String[] args) {
/*  */
		
		int factorial = 5;
		
		int factorialSum = 1;
		
		for(int i = 1; i <= factorial; i++) {
			
			factorialSum = factorialSum * i;
			
		}
		
		System.out.println("The factorial of " + factorial + " is " + factorialSum);

	}

}
