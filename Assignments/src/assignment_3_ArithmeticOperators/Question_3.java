package assignment_3_ArithmeticOperators;

public class Question_3 {

	public static void main(String[] args) {
		
		/* Write a Java program that displays the area of a rectangle with a width of 4.5 
		 * and a height of 7.9 using the following formula:
		 area = width * height*/
		
		double area; double width = 4.5; double height = 7.9;
		
		area = width * height;
		
		System.out.println("The area of a rectangle with 4.5 width and 7.9 height is " + area);

	}

}
